def root():
    """Root endpoint."""
    return {
        "message": "Yoga RAG API",
        "docs": "/docs",
        "endpoints": {"query": "/query"},
    }


@app.get("/health")
def health():
    """Health check endpoint."""
    return {"status": "healthy"}


@app.post("/query", response_model=QueryResponse)
def query_endpoint(request: QueryRequest):
    """Query the yoga knowledge base.

    Args:
        request: QueryRequest with question string

    Returns:
        QueryResponse with generated answer and metadata
    """
    try:
        # Retrieve relevant documents
        documents = retrieve_documents(request.question, request.top_k)

        # Generate answer
        raw_answer = generate_answer(request.question, documents)
        pose_entries, answer = extract_pose_block(raw_answer)
        print(f"Pose entries parsed from LLM response: {pose_entries}")

        # Build response
        sources = extract_sources(documents)
        poses = build_pose_cards(pose_entries)

        return QueryResponse(
            id=str(uuid.uuid4()),
            answer=answer,
            sources=sources,
            isUnsafe=False,  # Can be enhanced with safety check
            poses=poses,
        )

    except Exception as e:
        raise HTTPException(status_code=500, detail=str(e))


if __name__ == "__main__":
    import uvicorn

    uvicorn.run(app, host="0.0.0.0", port=PORT)
